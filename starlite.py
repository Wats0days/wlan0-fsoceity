def get_my_sim():
    """Fetches SIM info of the phone running the script."""
    try:
        res = subprocess.run(["termux-telephony-deviceinfo"], capture_output=True, text=True)
        # Extracting Network Operator (SIM)
        match = re.search(r'"network_operator_name": "([\w\s]+)"', res.stdout)
        return match.group(1) if match else "Unknown SIM"
    except:
        return "No SIM Access"

def deep_scan(subnet):
    print(f"{RED}[!] INITIATING DEEP SCAN (BYPASSING FIREWALLS)...")
    # -T4: Faster | -F: Fast port scan | -sV: Version detection (finds brands)
    # --osscan-guest: Tries to guess if it's a phone or PC
    cmd = ["nmap", "-T4", "-F", "-sV", "--osscan-guess", subnet]
    result = subprocess.run(cmd, capture_output=True, text=True)
    return result.stdout

def parse_device_info(nmap_output):
    """Parses Nmap output to find Brand Names and OS."""
    devices = []
    # Splitting output by host blocks
    blocks = nmap_output.split("Nmap scan report for ")
    for block in blocks[1:]:
        lines = block.split('\n')
        ip = re.search(r'([\d\.]+)', lines[0]).group(1)
        
        # Look for Vendor/Brand in MAC line or Service line
        brand = "Generic Device"
        if "Apple" in block or "iPhone" in block: brand = "iPhone/Mac"
        elif "Vivo" in block: brand = "VIVO"
        elif "Oppo" in block: brand = "OPPO"
        elif "Samsung" in block: brand = "SAMSUNG"
        elif "Microsoft" in block or "Windows" in block: brand = "WINDOWS PC"
        
        devices.append({"ip": ip, "brand": brand})
    return devices

def main():
    os.system('clear')
    print(BANNER)
    subprocess.run(["termux-wake-lock"])
    
    my_sim = get_my_sim()
    print(f"{GREEN}[+] YOUR SIM: {my_sim}")
    
    try:
        while True:
            # Get IP and determine subnet
            s = socket.socket(socket.AF_INET, socket.SOCK_DGRAM)
            s.connect(("8.8.8.8", 80))
            my_ip = s.getsockname()[0]
            subnet = '.'.join(my_ip.split('.')[:-1]) + '.0/24'
            s.close()

            print(f"\n{RED}{'—'*60}")
            raw_data = deep_scan(subnet)
            found = parse_device_info(raw_data)
            
            print(f"{GREEN}[+] DISCOVERED {len(found)} TARGETS:")
            for dev in found:
                color = GREEN if dev['ip'] == my_ip else RED
                print(f"{color}  > {dev['ip']} — TYPE: {dev['brand']}")
            
            time.sleep(60)
    except KeyboardInterrupt:
        subprocess.run(["termux-wake-unlock"])

if __name__ == "__main__":
    main()